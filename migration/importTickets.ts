async function getCustomerRefBySupabaseId(customerId: string) {
  const q = query(
    collection(db, "customers"),
    where("supabaseCustomerId", "==", customerId)
  );

  const snap = await getDocs(q);
  return snap.empty ? null : snap.docs[0];
}
export async function importTicketsToFirestore(
  supabaseTickets: SupabaseTicket[]
) {
  const ticketsRef = collection(db, "tickets");

  for (const ticket of supabaseTickets) {
    try {
      // ðŸ”¹ Find related customer
      const customerDoc = await getCustomerRefBySupabaseId(
        ticket.customer_id
      );

      if (!customerDoc) {
        console.warn(
          `âš ï¸ Customer not found for ticket ${ticket.id}`
        );
        continue;
      }

      // ðŸ”¹ Avoid duplicate tickets
      const existing = await getDocs(
        query(ticketsRef, where("supabaseTicketId", "==", ticket.id))
      );

      if (!existing.empty) {
        console.log(`â­ï¸ Ticket already exists: ${ticket.id}`);
        continue;
      }

      // ðŸ”¹ Normalize device
      const device = {
        type: ticket.device?.type || "UNKNOWN",
        brand: ticket.device?.brand ?? "",
        brandService: ticket.device?.brandService ?? "",
        model: ticket.device?.model ?? "",
        serialNumber: ticket.device?.serialNumber ?? "",
        description: ticket.device?.description ?? "",
      };

      // ðŸ”¹ Add ticket
      await addDoc(ticketsRef, {
        supabaseTicketId: ticket.id,

        customerId: customerDoc.id, // Firestore ref
        customerName: customerDoc.data().name || "",

        subject: ticket.subject || "",
        status: ticket.status,
        priority: ticket.priority,

        holdReason: ticket.hold_reason ?? null,
        internalProgressReason:
          ticket.internal_progress_reason ?? null,

        assignedTo: ticket.assigned_to ?? null,

        device,
        chargerStatus: ticket.charger_status,
        store: ticket.store,

        amountEstimate: ticket.amount_estimate || 0,
        warranty: ticket.warranty,

        billNumber: ticket.bill_number ?? null,
        scheduledDate: ticket.scheduled_date
          ? new Date(ticket.scheduled_date)
          : null,

        createdAt: new Date(ticket.created_at),
        resolvedAt: ticket.resolved_at
          ? new Date(ticket.resolved_at)
          : null,

        source: "supabase",
        createdOn: serverTimestamp(),
      });

      console.log(`âœ… Imported ticket ${ticket.id}`);
    } catch (err: any) {
      console.error(
        `âŒ Failed ticket ${ticket.id}:`,
        err.message
      );
    }
  }

  console.log("ðŸŽ‰ All tickets imported successfully");
}
